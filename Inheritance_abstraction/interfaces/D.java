package pkg.Inheritance_abstraction.interfaces;

public class D extends B {

	@Override
	public void doThat() {
		System.out.println("dothat got executed");
		
	}

	@Override
	public void doThis() {
		System.out.println("dothis got executed");
		
	}

	@Override
	public void doThose() {
		System.out.println("dothose got executed");
		
	}

}
