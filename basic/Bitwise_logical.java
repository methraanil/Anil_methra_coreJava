package pkg.basic;

public class Bitwise_logical {
	public static void main(String args[]){
		boolean a=true;
		boolean b=false;
		
		System.out.println(a&&b);
		System.out.println(a||b);
		System.out.println(!a);
		
		int num1=30;
		int num2=40;
		
		System.out.println(num1&num2);
		System.out.println(num1|num2);
		
		System.out.println(~num1);
	}

}
