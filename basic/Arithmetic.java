package pkg.basic;

public class Arithmetic {

	public static void main(String[] args) {
		int n=1;
		n++;
		System.out.println(n);
		n=n+1;
		System.out.println(n);
		
		n--;
		System.out.println(n);
		n=n-1;
		System.out.println(n);
		
		n+=5;
		System.out.println(n);
		n=n+5;
		System.out.println(n);
		n-=10;
		System.out.println(n);
		n=n-10;
		System.out.println(n);
		
		n*=2;
		System.out.println(n);
		n=n*2;
		System.out.println(n);
		n/=5;
		System.out.println(n);
		n=n/5;
		
		System.out.println(n);

	}

}
